def seed_categories(db, Category, Subcategory, Item):
    for cat_idx, cat_data in enumerate(CATEGORIES_DATA):
        category_image_url = f"https://res.cloudinary.com/playback-assets/image/upload/{cat_data['slug']}.png"

        existing = Category.query.filter_by(slug=cat_data["slug"]).first()
        if existing:
            print(f"  [UPDATE] Categoría ya existe: {cat_data['name']['es']}")
            existing.position = cat_idx
            existing.name = cat_data["name"]
            existing.description = cat_data["description"]
            existing.image_url = category_image_url
            category = existing
        else:
            category = Category(
                name=cat_data["name"],
                slug=cat_data["slug"],
                description=cat_data["description"],
                image_url=category_image_url,
                position=cat_idx,
            )
            db.session.add(category)
            print(f"  [OK] Categoría creada: {cat_data['name']['es']}")
        db.session.flush()

        for sub_idx, sub_data in enumerate(cat_data["subcategories"]):
            subcategory_image_url = f"https://res.cloudinary.com/playback-assets/image/upload/{sub_data['slug']}.png"

            existing_sub = Subcategory.query.filter_by(slug=sub_data["slug"]).first()
            if existing_sub:
                print(f"         [UPDATE] Subcategoría ya existe: {sub_data['name']['es']}")
                existing_sub.position = sub_idx
                existing_sub.name = sub_data["name"]
                existing_sub.description = sub_data["description"]
                existing_sub.image_url = subcategory_image_url
                subcategory = existing_sub
            else:
                subcategory = Subcategory(
                    name=sub_data["name"],
                    slug=sub_data["slug"],
                    description=sub_data["description"],
                    image_url=subcategory_image_url,
                    category=category,
                    position=sub_idx,
                )
                db.session.add(subcategory)
                print(f"         [OK] Subcategoría creada: {sub_data['name']['es']}")
            db.session.flush()

            for item_idx, item_data in enumerate(sub_data.get("items", [])):
                item_image_url = f"https://res.cloudinary.com/playback-assets/image/upload/{item_data['slug']}.png"

                existing_item = Item.query.filter_by(slug=item_data["slug"]).first()
                if existing_item:
                    print(f"                [UPDATE] Item ya existe: {item_data['name']['es']}")
                    existing_item.position = item_idx
                    existing_item.name = item_data["name"]
                    existing_item.image_url = item_image_url
                else:
                    item = Item(
                        name=item_data["name"],
                        slug=item_data["slug"],
                        description=None,
                        image_url=item_image_url,
                        subcategory=subcategory,
                        position=item_idx,
                    )
                    db.session.add(item)
                    print(f"                [OK] Item creado: {item_data['name']['es']}")

    db.session.commit()
    print("\n✅ Seed completado con éxito.")


